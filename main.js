function compare(a, b) {
    if (a > b) return 1;
    if (b > a) return -1;

    return 0;
}

function decrypt()
{
    var alphabet = 'abcdefghijklmnopqrstuvwxyz';
    var withoutOne = ['e','n','r','s','t','c','y','k','d','p','m','j','l','b','g','h','f','q','v']; //sorted by frequencies in Polish
    var availableOne = ['i', 'z', 'w', 'a', 'u', 'o']; //sorted by frequencies in Polish
    var lettersFromText = [];
    var singleLettersFromText = [];
    var frequencies = [];
    var code = [];
    var decoded = '';

    /**
     * Count letters from text
     */
    for (i=0; i<text.length; i++)
    {
        /**
         * Continue if letter is not letter from alphabet
         */
        if (alphabet.includes(text.charAt(i)) === false)
        {
            continue;
        }

        /**
         * Get and continue if alone letter
         */
        if (text.charAt(i-1) === ' ' && text.charAt(i+1) === ' ' && singleLettersFromText.includes(text.charAt(i)) === false)
        {
            singleLettersFromText.push(text.charAt(i));
            continue;
        }

        /**
         * Add letter to array
         */
        lettersFromText[text.charAt(i)] ? lettersFromText[text.charAt(i)]++ : lettersFromText[text.charAt(i)] = 1;
    }

    /**
     * Add single letters from lettersFromText array to singleLettersFromText array
     * Delete single letters from letters array
     */
    Object.keys(lettersFromText).forEach(function(key) {
        if (singleLettersFromText.includes(key))
        {
            delete lettersFromText[key];
        }
    });

    for (i=0; i<alphabet.length; i++)
    {
        if (typeof lettersFromText[alphabet.charAt(i)] !== 'undefined')
        {
            frequencies.push(lettersFromText[alphabet.charAt(i)]);
        }
    }

    frequencies = frequencies.sort(compare).reverse();

    for (i=0; i<withoutOne.length; i++)
    {
        for (j=0; j<Object.keys(alphabet).length; j++)
        {
            if (lettersFromText[alphabet.charAt(j)] === frequencies[i])
            {
                code[alphabet.charAt(j)] = withoutOne[i];
            }
        }

    }

    for (i=0; i<availableOne.length; i++)
    {
        code[singleLettersFromText[i]] = availableOne[i];
    }


    for (i=0; i<text.length; i++)
    {
        if (alphabet.includes(text.charAt(i)) === false && text.charAt(i) !== ' ')
        {
            continue;
        }

        if (text.charAt(i) === " ")
        {
            decoded += " ";
            continue;
        }

        decoded += code[text.charAt(i)]
    }

    $('#decodedText').text(decoded);

}


